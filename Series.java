
public class Series {

	public static void main(String[] args) {
		for(int i=35;i<47;i++)
		{
			if(i==38)
			{
				//in words
				System.out.println("Thirty Eight");
				continue;
			}
			else if(i==45)
			{
				System.out.println("Fourty Five");
				continue;
			}
			else
			{
				System.out.println(i);
			}
		}

	}

}
